package json

class PostUserResponse {
    private val name: String? = null
    private val job: String? = null
    private val id: String? = null
    private val createdAt: String? = null

    fun getName(): String? {
        return name
    }

    fun getJob(): String? {
        return job
    }

    fun getId(): String? {
        return id
    }

    fun getCreatedAt(): String? {
        return createdAt
    }
}